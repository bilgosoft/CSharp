// Math Functions : Matematiksel işlemleri yapmamızı sağlayan fonksiyonlardır.

object result;

result = Math.Abs(5); 				// Mutlak değeri hesaplar.
result = Math.Ceiling(5.5); 		// Yukarı tamsayı olarak yuvarlar.
result = Math.Floor(5.5);			// Aşağı tamsayı olarak yuvarlar.
result = Math.Pow(5,7);				// 1. sayı taban 2. üst olarak kuvvetini hesaplar.
result = Math.Sqrt(5);				// Sayının karekökünü hesaplar.
result = Math.Cos(90);				// Sayının cos karşılığı hesaplar.
result = Math.Tan(90);				// Sayının tan karşılığı hesaplar.
result = Math.Sin(90);				// Sayının sin karşılığı hesaplar.
result = Math.Max(1,2);				// Büyük sayıyı yazar.
result = Math.Min(1,2);				// Küçük sayıyı yazar.
result = Math.E;					// 2.71 olan matematiksel sayıyı ifade eder.
result = Math.Exp(2);				// Sayıyı, E sayısının kuvveti olarak hesaplar.
result = Math.PI;					// PI sayısının değerini ifade eder.
result = Math.Round(5.54145451,2);	// Virgülden sonra kaç basamak yazacağını belirler.
result = Math.Sign(5);				// Sayı pozitif ise 1, negatif ise -1, sıfır ise 0 yazar.
